                                    Генеральную уборку рекомендуется делать не реже чем раз в 4-6 месяцев. Оптимальный сценарий – сокращение периода в 2 раза. Предполагаются дополнительно тщательное мытье потолков, стен (если покрытие позволяет), тряска ковров.
