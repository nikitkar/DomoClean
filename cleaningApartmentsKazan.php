                                <p class="blockInfo--text">
                                    Уборка – это хорошо. Влажная, к примеру, исключает повышенное содержание пыли в воздухе. Она позволяет бороться с пылевыми клещами, бактериями. Проводить ее следует регулярно. И использовать при этом специальные моющие средства. Лучше, если они будут гипоаллергенными и экологически чистыми. Протирать нужно не только пол, столы, но и другую мебель, а также технику. Мыть требуется окна, двери, другие предметы интерьера.
                                    <br><br>
                                    Сухая уборка с помощью пылесоса должна быть комплексной, то есть предполагать и влажную после удаления основных загрязнений. Особенно важно это весной и летом, когда пыли, пыльцы становится больше обычного.
                                    <br><br>
                                    Влажной уборки заслуживают все помещения квартиры. Вещи, объективно не поддающиеся ей (шторы, другие), можно чистить с помощью вакуумного пылесоса.
                                    <br><br>
                                    Примерно раз в 2-3 недели рекомендуется уделять внимание и люстрам. Особенно если их формы являются сложными. То же касается отопительных устройств, внутренних поверхностей шкафов, тумб и не только. Пройтись пылесосом, тряпкой следует везде, где пыль и грязь скапливаются по разным причинам. Это пространство комнат под мебелью на ножках, углы, плинтусы.
